// 当前语言设置（默认中文）
let currentLocale: Locale = "zh-CN";

/**
 * 设置当前语言
 */
export function setLocale(locale: Locale): void {
  currentLocale = locale;
}

/**
 * 获取当前语言
 */
export function getLocale(): Locale {
  return currentLocale;
}

/**
 * 翻译函数
 * @param key 翻译键
 * @param fallback 备用文本（可选）
 * @returns 翻译后的文本
 */
export function t(key: string, fallback?: string): string {
  const dict = translations[currentLocale];
  return dict?.[key] ?? fallback ?? key;
}

/**
 * 初始化语言设置
 * 从浏览器语言或 localStorage 读取
 */
export function initLocale(): void {
  // 优先从 localStorage 读取
  const stored = localStorage.getItem("openclaw-ui-locale") as Locale | null;
  if (stored && (stored === "en" || stored === "zh-CN")) {
    currentLocale = stored;
    return;
  }

  // 从浏览器语言检测
  const browserLang = navigator.language || "zh-CN";
  if (browserLang.startsWith("zh")) {
    currentLocale = "zh-CN";
  } else {
    currentLocale = "en";
  }

  // 保存到 localStorage
  localStorage.setItem("openclaw-ui-locale", currentLocale);
}

/**
 * 切换语言
 */
export function toggleLocale(): void {
  currentLocale = currentLocale === "en" ? "zh-CN" : "en";
  localStorage.setItem("openclaw-ui-locale", currentLocale);
  // 触发重新渲染（需要在 app 层面处理）
  window.dispatchEvent(new CustomEvent("locale-changed"));
}
