// 当前语言设置（默认中文）
let currentLocale: Locale = "zh-CN";

/**
 * 设置当前语言
 */
export function setLocale(locale: Locale): void {
  currentLocale = locale;
}

/**
 * 获取当前语言
 */
export function getLocale(): Locale {
  return currentLocale;
}

/**
 * 翻译函数
 * @param key 翻译键
 * @param fallbackOrParams 备用文本或模板变量参数
 * @returns 翻译后的文本
 */
export function t(key: string, fallbackOrParams?: string | Record<string, unknown>): string {
  const dict = translations[currentLocale];
  let text: string;
  let params: Record<string, unknown> | undefined;

  // 判断第二个参数是字符串（fallback）还是对象（params）
  if (typeof fallbackOrParams === "string") {
    text = dict?.[key] ?? fallbackOrParams ?? key;
  } else {
    text = dict?.[key] ?? key;
    params = fallbackOrParams;
  }

  // 如果有模板变量参数，进行替换
  if (params) {
    Object.entries(params).forEach(([paramKey, value]) => {
      text = text.replace(new RegExp(`{${paramKey}}`, "g"), String(value));
    });
  }

  return text;
}

/**
 * 初始化语言设置
 * 从浏览器语言或 localStorage 读取
 */
export function initLocale(): void {
  // 优先从 localStorage 读取
  const stored = localStorage.getItem("openclaw-ui-locale") as Locale | null;
  if (stored && (stored === "en" || stored === "zh-CN")) {
    currentLocale = stored;
    return;
  }

  // 从浏览器语言检测
  const browserLang = navigator.language || "zh-CN";
  if (browserLang.startsWith("zh")) {
    currentLocale = "zh-CN";
  } else {
    currentLocale = "en";
  }

  // 保存到 localStorage
  localStorage.setItem("openclaw-ui-locale", currentLocale);
}

/**
 * 切换语言
 */
export function toggleLocale(): void {
  currentLocale = currentLocale === "en" ? "zh-CN" : "en";
  localStorage.setItem("openclaw-ui-locale", currentLocale);
  // 触发重新渲染（需要在 app 层面处理）
  window.dispatchEvent(new CustomEvent("locale-changed"));
}
